/**
 * Merges a source data model with a target data model updating
 * and adding values from the target data model that match the source
 * data model.  NOTHING is removed from the source data model.  If it
 * does not exist in the target data model, it remains in the source 
 * data model unchanged.
 * 
 * @author Emily
 *
 */
public class DataModelMergeAndUpdater {

	public static enum I18NMessages{
		MERGE_TASKNAME,
		MERGE_CAT_TASKNAME,
		ATT_NOT_FOUND,
		MERGE_ATT_TASKNAME,
		ATT_TYPE_MISMATCH
	}
	
	private SimpleDataModel sourceDm;
	private SimpleDataModel targetDm;
	
	private Language currentSystemLanguage; 
	private List<String> warnings;
	
	private Locale locale = null;
	private Session session;
	
	/**
	 * 
	 * @param source the data model to update; items are added to this data model.  The conservation area property of this data model must be set.
	 * @param target the data model to update from; this data model is not modified.  This conservation area property of this data model is optional
	 */
	public DataModelMergeAndUpdater(SimpleDataModel source, SimpleDataModel target){
		if (source.getConservationArea() == null) throw new IllegalStateException("Conservation area property of data model object not set."); //$NON-NLS-1$
		this.sourceDm = source;
		this.targetDm = target;
		locale = Locale.getDefault();
	}
	
	/**
	 * 
	 * @param source the source data model to update.  The conservation area property of this data model must be set.
	 * @param target the target data model (this data model is not modified)
	 * @param currentSystemLanguage the current system language (can be null)
	 */
	public DataModelMergeAndUpdater(SimpleDataModel source, SimpleDataModel target, Language currentSystemLanguage){
		this(source, target);
		this.currentSystemLanguage = currentSystemLanguage;
	}
	
	/**
	 * 
	 * @param source the source data model to update.  The conservation area property of this data model must be set.
	 * @param target the target data model (this data model is not modified)
	 * @param l the locale for system messages (null uses the default locale)
	 */
	public DataModelMergeAndUpdater(SimpleDataModel source, SimpleDataModel target, Locale l){
		this(source, target);
		this.locale = l;
	}
	
	private String getMessage(I18NMessages message) {
		return SmartContext.INSTANCE.getClass(ICoreLabelProvider.class).getLabel(message, locale);
	}
	
	/**
	 * Merges the source and target datamodel updating the
	 * source data model as required.
	 * 
	 * If an exception is thrown the state of the source data model
	 * cannot be guarenteed. It may be partially modified and should be
	 * reloaded if used after the merge state.
	 * 
	 * @return list of warnings generated by the process
	 */
	public List<String> merge(Session session, IProgressMonitor monitor) throws Exception{
		this.session = session;
		
		warnings = new ArrayList<String>();
		monitor.beginTask(getMessage(I18NMessages.MERGE_TASKNAME), targetDm.getAttributes().size() + targetDm.getCategories().size() +1);
		
		
		//update icon Conservation area links
		targetDm.getCategories().forEach(c->{
			c.accept(e->{updateIconCa(e, sourceDm.getConservationArea()); return true;});
		});
		targetDm.getAttributes().forEach(a->{
			updateIconCa(a, sourceDm.getConservationArea());
			if (a.getAttributeList() != null) a.getAttributeList().forEach(item->updateIconCa(item, sourceDm.getConservationArea()));
			if (a.getTree() != null) {
				a.getTree().forEach(node->{
					node.accept(e->{updateIconCa((DmObject)e, sourceDm.getConservationArea()); return true;});
				});
			}
		});
		
		monitor.subTask(getMessage(I18NMessages.MERGE_ATT_TASKNAME));
		mergeAttributes(monitor);
	
		monitor.subTask(getMessage(I18NMessages.MERGE_CAT_TASKNAME));
		List<Category> visits = new ArrayList<>();
		visits.addAll(targetDm.getCategories());
		visits.addAll(sourceDm.getCategories());
		
		while(!visits.isEmpty()) {
			Category c = visits.remove(0);
			c.getNames().size();	//this seems to be necessary to avoid hibernate issues in the next section
			if (c.getChildren() != null) visits.addAll(c.getChildren());
		}
		
		for (Category targetCategory : targetDm.getCategories()){
			boolean found = false;
			for (Category sourceCategory : sourceDm.getCategories()){
				if (sourceCategory.getKeyId().equals(targetCategory.getKeyId())){
					found = true;
					mergeCategory(sourceCategory, targetCategory);
					break;
				}
			}
			if (!found){
				//new category
				Category newSource = createNewCategory(targetCategory);
				sourceDm.getCategories().add(newSource);
				mergeCategory(newSource, targetCategory);
			}
			monitor.worked(1);
		}
		
		//ensure correct categoryattribute links are created
		//8.1.0 -> need categoryattribute objects for all children as well as parents
		//1. check that the is root node is set correct
		//for all the categories for all the attributes if an attribute exists further up the 
		//tree it can't be root
		List<Category> toProcess = new ArrayList<>();
		toProcess.addAll(sourceDm.getCategories());
		while(!toProcess.isEmpty()) {
			Category c = toProcess.remove(0);
			toProcess.addAll(c.getChildren());
			
			for (CategoryAttribute ca : c.getAllAttributes()) {
				Category parent = ca.getCategory().getParent();
				boolean isroot = true;
				while(parent != null) {
					if (parent.hasAttribute(ca.getAttribute())) {
						isroot = false;
						break;
					}
					parent = parent.getParent();
				}
				ca.setIsRoot(isroot);
			}
		}
		//now for all root attributes ensure they are in all the kids
		toProcess.clear();
		toProcess.addAll(sourceDm.getCategories());
		while(!toProcess.isEmpty()) {
			Category c = toProcess.remove(0);
			toProcess.addAll(c.getChildren());
			
			for (CategoryAttribute ca : c.getAllAttributes()) {
				if (!ca.getIsRoot()) continue;
				
				//ensure this attributes exists as not root in all children categores
				List<Category> kids = new ArrayList<>(ca.getCategory().getChildren());
				while(!kids.isEmpty()) {
					Category kid = kids.remove(0);
					kids.addAll(kid.getChildren());
					CategoryAttribute tmp = kid.findAttribute(ca.getAttribute());
					if (tmp == null) {
						tmp = new CategoryAttribute(kid, ca.getAttribute());
						tmp.setIsActive(kid.getIsActive() && ca.getIsActive());
						tmp.setOrder(kid.getAllAttributes().size()+1);
						kid.getAllAttributes().add(tmp);
					}
					tmp.setIsRoot(false);					
				}
			}
		}
		//sort all attributes
		toProcess.clear();
		toProcess.addAll(sourceDm.getCategories());
		while(!toProcess.isEmpty()) {
			Category c = toProcess.remove(0);
			toProcess.addAll(c.getChildren());
			resortAttribute(c.getAllAttributes());
			resortCategories(c.getChildren());
		}
		resortCategories(sourceDm.getCategories());
		
		monitor.done();
		return warnings;
	}
	
	private void updateIconCa(DmObject object, ConservationArea ca) {
		Icon icon = object.getIcon();
		
		if (icon == null) return;
		icon.setConservationArea(ca);
		
		//update labels - ensure language references ca language
		List<Label> toRemove = new ArrayList<>();
		for (Label l : icon.getNames()) {
			if (ca.equals(l.getLanguage().getCa())) continue;
			
			Language updateto = null;
			for (Language lang : ca.getLanguages() ) {
				if (lang.getCode().equalsIgnoreCase(l.getLanguage().getCode())) {
					updateto = lang;
					break;
				}
			}
			if (updateto == null) {
				toRemove.add(l);
			}else {
				l.setLanguage(updateto);
			}
		}
		if (!toRemove.isEmpty()) icon.getNames().removeAll(toRemove);
		//make sure there is a name for the default language
		if (icon.findNameNull(ca.getDefaultLanguage()) == null) {
			if (icon.getNames().isEmpty()) {
				icon.updateName(ca.getDefaultLanguage(), "icon"); //$NON-NLS-1$
			}else {
				icon.updateName(ca.getDefaultLanguage(), icon.getNames().iterator().next().getValue());
			}
			
		}
	}
	
	private void mergeCategory(Category source, Category target) throws Exception{
		if (!source.getKeyId().equals(target.getKeyId())) return;
		if (!source.getHkey().equals(target.getHkey())) return;
		
		source.setIsActive(target.getIsActive());
		source.setIsMultiple(target.getIsMultiple());
		source.setCategoryOrder(target.getCategoryOrder());
		if (target.getIcon() != null) {
			setIcon(source, target.getIcon());
		}
		
		mergeNames(source, target);
		
		if (target.getAllAttributes() != null){
			for (CategoryAttribute targetAttribute : target.getAllAttributes()){
				boolean found = false;
				for (CategoryAttribute sourceAttribute : source.getAllAttributes()){
					if (targetAttribute.getAttribute().getKeyId().equals(sourceAttribute.getAttribute().getKeyId())){
						mergeCategoryAttribute(sourceAttribute, targetAttribute);
						found = true;
						break;
					}
				}
				if (!found){
					Attribute newSourceAttribute = findAttributeInSource(targetAttribute.getAttribute().getKeyId());
					if (newSourceAttribute == null){
						warnings.add(MessageFormat.format(getMessage(I18NMessages.ATT_NOT_FOUND), targetAttribute.getAttribute().getKeyId(),source.getName())); 
					}else{
						CategoryAttribute newSourceCa = new CategoryAttribute(source, newSourceAttribute);
						source.getAllAttributes().add(newSourceCa);
						mergeCategoryAttribute(newSourceCa, targetAttribute);
					}
				}
			}
		}
		
		if (target.getChildren() != null){
			for (Category targetChild : target.getChildren()){
				boolean found = false;
				for (Category sourceChild : source.getChildren()){
					if (targetChild.getKeyId().equals(sourceChild.getKeyId())){
						found = true;
						mergeCategory(sourceChild, targetChild);
						break;
					}
				}
				if (!found){
					//create new category
					Category newSource = createNewCategory(targetChild);
					newSource.setParent(source);
					source.getChildren().add(newSource);
					mergeCategory(newSource, targetChild);
				}
			}
		}
	}
	
	private Category createNewCategory(Category target){
		Category newSource = new Category();
		newSource.setKeyId(target.getKeyId());
		newSource.setHkey(target.getHkey());
		newSource.setConservationArea(sourceDm.getConservationArea());
		newSource.setCategoryOrder(target.getCategoryOrder());
		newSource.setChildren(new ArrayList<Category>());
		newSource.setAllAttributes(new ArrayList<CategoryAttribute>());
		setIcon(newSource, target.getIcon());
		return newSource;
	}
	
	private void mergeCategoryAttribute(CategoryAttribute source, CategoryAttribute target){
		source.setIsActive(target.getIsActive());
		source.setOrder(target.getOrder());
		source.setIsRoot(target.getIsRoot());
	}
	
	private Attribute findAttributeInSource(String keyId){
		for (Attribute a : sourceDm.getAttributes()){
			if (a.getKeyId().equals(keyId)){
				return a;
			}
		}
		return null;
	}
		
	private void mergeAttributes(IProgressMonitor monitor){
		for(Attribute targetAttribute : targetDm.getAttributes()){
			boolean found = false;
			for(Attribute sourceAttribute : sourceDm.getAttributes()){
				if (targetAttribute.getKeyId().equals(sourceAttribute.getKeyId())){
					found = true;
					mergeAttribute(sourceAttribute, targetAttribute);
				}
			}
			if (!found){
				//create new attribute
				Attribute newSource = new Attribute();
				newSource.setKeyId(targetAttribute.getKeyId());
				newSource.setType(targetAttribute.getType());
				newSource.setConservationArea(sourceDm.getConservationArea());
				setIcon(newSource, targetAttribute.getIcon());
				sourceDm.getAttributes().add(newSource);
				mergeAttribute(newSource, targetAttribute);
			}
			monitor.worked(1);
		}
	}
	
	private void setIcon(DmObject source, Icon target) {
		if (target == null) return;
		if (target.getUuid() == null) {
			session.persist(target);
		}
		source.setIcon(target);
	}
	
	private void mergeAttribute(Attribute source, Attribute target){
		if (!source.getKeyId().equals(target.getKeyId())) return;
		
		if (!source.getType().equals(target.getType())){
			warnings.add(MessageFormat.format(getMessage(I18NMessages.ATT_TYPE_MISMATCH), source.getKeyId(), source.getType().name(), target.getType().name()));
			return;
		}
		mergeNames(source, target);
		source.setIsRequired(target.getIsRequired());
		if (target.getIcon() != null) setIcon(source, target.getIcon());
		
		if (source.getType() == AttributeType.NUMERIC){
			if (target.getMinValue() != null){
				source.setMinValue(target.getMinValue());
			}
			if (target.getMaxValue() != null){
				source.setMaxValue(target.getMaxValue());
			}
			if (source.getAggregations() == null){
				source.setAggregations(new ArrayList<Aggregation>());
			}
			for (Aggregation agg : target.getAggregations()){
				if (!source.getAggregations().contains(agg)){
					source.getAggregations().add(agg);
				}
			}
			
		}else if (source.getType() == AttributeType.TEXT){
			if (target.getRegex() != null && !target.getRegex().isEmpty()){
				source.setRegex(target.getRegex());
			}
			
		}else if (source.getType().isList()) {
			mergeListItems(source, target);
			
		}else if (source.getType() == AttributeType.TREE){
			mergeAttributeTreeNodes(source, target);
			syncEnabledTree(source);
		}
	}
	
	private void mergeAttributeTreeNodes(Attribute source, Attribute target){
		if (!source.getKeyId().equals(target.getKeyId())) return;
		if (target.getTree() == null) return;
		
		for(AttributeTreeNode targetNode : target.getTree()){
			boolean found = false;
			if (source.getTree() != null){
				for(AttributeTreeNode sourceNode : source.getTree()){
					if (sourceNode.getKeyId().equals(targetNode.getKeyId())){
						found = true;
						mergeTreeNodes(sourceNode, targetNode);
						break;
					}	
				}
			}else{
				source.setTree(new ArrayList<AttributeTreeNode>());
			}
			
			if (!found){
				//create new node
				AttributeTreeNode newSourceKid =createNewTreeNode(targetNode);
				newSourceKid.setAttribute(source);
				newSourceKid.setParent(null);				
				source.getTree().add(newSourceKid);
				
				mergeTreeNodes(newSourceKid, targetNode);	
			}
		}
		resortTreeNodes(source.getTree());
	}
	
	private void mergeTreeNodes(AttributeTreeNode source, AttributeTreeNode target){
		if (!source.getKeyId().equals(target.getKeyId())) return;
		if (!source.getAttribute().getKeyId().equals(target.getAttribute().getKeyId())) return;
		if ((source.getParent() == null && target.getParent() != null) ||
			(source.getParent() != null && target.getParent() == null) ||
			(source.getParent() != null && target.getParent() != null && 
			  !source.getParent().getKeyId().equals(target.getParent().getKeyId()))) return;
		
		mergeNames(source, target);
		source.setIsActive(target.getIsActive());
		source.setNodeOrder(target.getNodeOrder());
		if (target.getIcon() != null) setIcon(source, target.getIcon());
		
		//process kids
		if (target.getChildren() != null){
			for (AttributeTreeNode targetKid : target.getChildren()){
				boolean found = false;
				if (source.getChildren() == null){
					source.setChildren(new ArrayList<AttributeTreeNode>());
				}
				for (AttributeTreeNode sourceKid : source.getChildren()){
					if (targetKid.getKeyId().equals(sourceKid.getKeyId())){
						found = true;
						mergeTreeNodes(sourceKid, targetKid);
						break;
					}
				}
				if(!found){
					//create a new node and merge
					AttributeTreeNode newSourceKid = createNewTreeNode(targetKid);
					newSourceKid.setAttribute(source.getAttribute());
					newSourceKid.setParent(source);
					setIcon(newSourceKid, source.getIcon());
					source.getChildren().add(newSourceKid);
					mergeTreeNodes(newSourceKid, targetKid);	
				}
			}
		}
			
		//re-order
		List<AttributeTreeNode> kids = source.getChildren();
		resortTreeNodes(kids);
	}
	
	private AttributeTreeNode createNewTreeNode(AttributeTreeNode targetNode){
		AttributeTreeNode newSourceKid = new AttributeTreeNode();
		newSourceKid.setKeyId(targetNode.getKeyId());
		newSourceKid.setHkey(targetNode.getHkey());
		newSourceKid.setChildren(new ArrayList<AttributeTreeNode>());
		newSourceKid.setNodeOrder(targetNode.getNodeOrder());
		setIcon(newSourceKid, targetNode.getIcon());
		return newSourceKid;
	}
	
	private void mergeListItems(Attribute source, Attribute target){
		if (target.getAttributeList() == null) return;
		
		if (source.getAttributeList() == null){
			source.setAttributeList(new ArrayList<AttributeListItem>());
		}
		
		for(AttributeListItem targetItem : target.getAttributeList()){
			boolean found = false;
			for (AttributeListItem sourceItem : source.getAttributeList()){
				if (sourceItem.getKeyId().equals(targetItem.getKeyId())){
					found = true;
					mergeListItem(sourceItem, targetItem);
					break;
				}
			}
			if (!found){
				//create new List Item
				AttributeListItem item = new AttributeListItem();
				item.setAttribute(source);
				item.setKeyId(targetItem.getKeyId());
				setIcon(item, source.getIcon());
				mergeListItem(item, targetItem);
				source.getAttributeList().add(item);
			}
		}
			
		//re-order
		resortListItems(source.getAttributeList());
	}
	
	private void mergeListItem(AttributeListItem source, AttributeListItem target){
		if (!source.getKeyId().equals(target.getKeyId())) return;
		if (!source.getAttribute().getKeyId().equals(target.getAttribute().getKeyId())) return;
		if (target.getIcon() != null) setIcon(source, target.getIcon());
		mergeNames(source, target);
		source.setListOrder(target.getListOrder());
		source.setIsActive(target.getIsActive());
	}
	
	private void mergeNames(NamedItem source, NamedItem target){
		String defaultValue = null;
		for (Language l : sourceDm.getConservationArea().getLanguages()) {
			if (!l.isDefault()) continue;
			
			for (Label trgLabel : target.getNames()) {
				if (l.getCode().equals(trgLabel.getLanguage().getCode())) {
					defaultValue = trgLabel.getValue();
					break;
				}
			}
			break;
		}
		
		
		for (Language l : sourceDm.getConservationArea().getLanguages()){
			for (Label trgLabel : target.getNames()) {
				if (trgLabel.getLanguage().getCode().equals(l.getCode())){
					source.updateName(l, trgLabel.getValue());
				}
			}
		}
		
		Language defaultLanguage = sourceDm.getConservationArea().getDefaultLanguage();
		if (source.findNameNull(defaultLanguage) == null) {
			if (defaultValue != null) {
				source.updateName(defaultLanguage, defaultValue);
			}else {
				if (!source.getNames().isEmpty()) {
					source.updateName(defaultLanguage, source.getNames().iterator().next().getValue());
				}else {
					source.updateName(defaultLanguage, target.getNames().iterator().next().getValue());
				}
			}
		}
		if (source.getName() == null) {
			//we want to update the name to match
			//this is only going to work if the current system language is from the same conservation
			//area as the source data model.  Otherwise this will always return null.  This is
			//okay for us as we only need the name set for the current conservation area being displayed
			source.setName(source.findNameNull(currentSystemLanguage));
		}
	}
	
	private int compareNames(NamedItem o1, NamedItem o2){
		return Collator.getInstance().compare(o1.findName(sourceDm.getConservationArea().getDefaultLanguage()), o2.findName(sourceDm.getConservationArea().getDefaultLanguage()));
	}
	private void resortCategories(List<Category> kids){
		Collections.sort(kids, new Comparator<Category>(){
			@Override
			public int compare(Category o1, Category o2) {
				int ret = ((Integer)o1.getCategoryOrder()).compareTo(o2.getCategoryOrder());
				if (ret != 0) return ret;
				return compareNames(o1, o2);
			}
		});
		int order = 0;
		for (Category i : kids){
			i.setCategoryOrder(order++);
		}
	}
	
	private void resortTreeNodes(List<AttributeTreeNode> kids){
		Collections.sort(kids, new Comparator<AttributeTreeNode>(){
			@Override
			public int compare(AttributeTreeNode o1, AttributeTreeNode o2) {
				int ret = ((Integer)o1.getNodeOrder()).compareTo(o2.getNodeOrder());
				if (ret != 0) return ret;
				return compareNames(o1, o2);
			}
		});
		int order = 0;
		for (AttributeTreeNode i : kids){
			i.setNodeOrder(order++);
		}
	}
	
	/**
	 * Syncs the enabled state of the tree.  It is possible that
	 * and update will disable a parent node without disabling a child
	 * node.  This will sync the state so that children of parent nodes
	 * that are disabled are disabled.
	 *  
	 * @param attribute
	 */
	private void syncEnabledTree(Attribute attribute){
		if (attribute.getTree() == null) return;
		
		List<AttributeTreeNode> toProcess = new ArrayList<AttributeTreeNode>();
		toProcess.addAll(attribute.getTree());
		while(!toProcess.isEmpty()){
			AttributeTreeNode node = toProcess.remove(0);
			if (node.getParent() != null 
					&& !node.getParent().getIsActive()
					&& node.getIsActive()){
				node.setIsActive(false);
			}
			if (node.getChildren() != null){
				toProcess.addAll(node.getChildren());
			}
		}
		
	}
	private void resortAttribute(List<CategoryAttribute> kids){
		Collections.sort(kids, (a,b)->{
				int ret = Integer.compare(a.getOrder(), b.getOrder());
				if (ret != 0) return ret;
				return compareNames(a.getAttribute(), b.getAttribute());
			});
		int order = 1;
		for (CategoryAttribute i : kids){
			i.setOrder(order++);
		}
	}
	
	private void resortListItems(List<AttributeListItem> kids){
		Collections.sort(kids, new Comparator<AttributeListItem>(){
			@Override
			public int compare(AttributeListItem o1, AttributeListItem o2) {
				int ret = ((Integer)o1.getListOrder()).compareTo(o2.getListOrder());
				if (ret != 0) return ret;
				return compareNames(o1, o2);
			}
		});
		int order = 0;
		for (AttributeListItem i : kids){
			i.setListOrder(order++);
		}
	}
}
