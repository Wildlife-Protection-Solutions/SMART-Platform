package org.wcs.smart.map.raster;

import java.awt.Transparency;
import java.awt.color.ColorSpace;
import java.awt.image.BufferedImage;
import java.awt.image.ColorModel;
import java.awt.image.ComponentColorModel;
import java.awt.image.ComponentSampleModel;
import java.awt.image.DataBuffer;
import java.awt.image.DataBufferFloat;
import java.awt.image.Raster;
import java.awt.image.SampleModel;
import java.awt.image.WritableRaster;
import java.io.BufferedWriter;
import java.io.IOException;
import java.io.OutputStream;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.ArrayList;
import java.util.Collection;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

import org.geotools.coverage.grid.GridCoverage2D;
import org.geotools.coverage.grid.GridCoverageFactory;
import org.geotools.gce.geotiff.GeoTiffWriter;
import org.geotools.geometry.Envelope2D;
import org.opengis.referencing.crs.CoordinateReferenceSystem;
import org.wcs.smart.util.SharedUtils;

import it.geosolutions.jaiext.range.NoDataContainer;


/**
 * Builds a raster file for one band.
 * 
 * <pre>
 * Coordinates in pixel values
 * (0,0) ----------- >(0,Max X)
 *   |
 *   |
 *   |
 *   |
 *   V
 * (0,Max Y) 
 * </pre>
 * 
 * 
 * 
 * @author Mauricio Pazos
 * @author Emily
 *
 */
public class RasterBuilder {
	
	private static final float NO_DATA = -9999;	//no data value
	
	/** a table where x,y values are the position in the raster grid. (0,0) is the bottom left tile and (360 180) is the top right tile */
	private Collection<? extends GridResultItem> table; 
	private GridMetadata metadata;
	
	private Path file;
	private String fileName;
	private Envelope2D envelope = null;
	
	/** tracks all files generated by this raster builder */
	private List<Path> allFiles;

	/** raster dimensions (pixel values) */
	private int width;
	private int height;

	/** grid cell size */
	private double gridCellSize;
	
	/**
	 * Sets the file name to write the raster to
	 * @param fileName
	 */
	public void setFileName(String fileName) {
		this.fileName = fileName;
		this.allFiles = new ArrayList<>();
	}
	
	/**
	 * 
	 * @param width 
	 * @param height 
	 */
	public void setRasterDimensions( final int width, final int height){
		assert width > 0 ;
		assert height > 0;
		
		this.width = width;
		this.height = height;
	}

	/**
	 * The envelop for the raster to generate.
	 * 
	 * @param envelop an envelope sets as EPSG:4326 is expected
	 */
	public void setEnvelope(Envelope2D envelop){
		this.envelope = envelop;
	}
	
	/**
	 * Sets the table used to build the raster
	 * 
	 * @param table query result data
	 * @param metadata query result metadata
	 */
	public void setTable(Collection<? extends GridResultItem> data, GridMetadata metadata ) {
		this.table = data;
		this.metadata = metadata;
	}
	
	/**
	 * Sets the grid cell size.
	 * 
	 * @param cellSize
	 */
	public void setGridCellSize(double cellSize){
		this.gridCellSize = cellSize;
	}

	/**
	 * Creates the raster file using the values present in the table.
	 * @throws Exception 
	 * 
	 * @throws NumberFormatException 
	 */
	public void build() throws Exception {
		
		assert this.table != null;
		assert this.envelope != null;

		BufferedImage raster = null;
		try {
			
			raster = createRaster();
			Path out = Paths.get(fileName);
			
			try(OutputStream fout = Files.newOutputStream(out)){
				GeoTiffWriter writer = new GeoTiffWriter(fout);

				GridCoverageFactory gcf = new GridCoverageFactory();
				Map<String, Object> properties = new HashMap<>();
				properties.put(NoDataContainer.GC_NODATA, new NoDataContainer(NO_DATA));
				GridCoverage2D inputCov = gcf.create("smartraster", raster, envelope, null, null, properties); //$NON-NLS-1$

				writer.write(inputCov, null);
			}
			
			allFiles.add(out);
			String baseFile = out.toString().substring(0,  out.toString().lastIndexOf('.'));
			createProjectionFile(baseFile, envelope.getCoordinateReferenceSystem());
			createWorldFile(baseFile, gridCellSize, envelope.getMinX(), envelope.getMaxY());
			this.file = out;

		}catch (Exception ex){
			throw ex;
		}
	}

	/**
	 * Writes required world file
	 * @param baseFile
	 * @param gridSize
	 * @param xmin
	 * @param ymax
	 * @throws IOException
	 */
	private void createWorldFile(final String baseFile,
			double gridSize, double xmin, double ymax) throws IOException {
		final Path prjFile = Paths.get(new StringBuffer(baseFile).append(".tfw") //$NON-NLS-1$
				.toString());
		allFiles.add(prjFile);
		try(BufferedWriter out = Files.newBufferedWriter(prjFile)){
			out.write(String.valueOf(gridSize));
			out.write(SharedUtils.LINE_SEPARATOR);
			out.write("0"); //$NON-NLS-1$
			out.write(SharedUtils.LINE_SEPARATOR);
			out.write("0"); //$NON-NLS-1$
			out.write(SharedUtils.LINE_SEPARATOR);
			out.write(String.valueOf(-gridSize));
			out.write(SharedUtils.LINE_SEPARATOR);
			out.write(String.valueOf(xmin));
			out.write(SharedUtils.LINE_SEPARATOR);
			out.write(String.valueOf(ymax));
			out.write(SharedUtils.LINE_SEPARATOR);
		}

	}
	
	/**
	 * Writes projection file
	 * @param baseFile
	 * @param coordinateReferenceSystem
	 * @throws IOException
	 */
	private void createProjectionFile(final String baseFile,
			final CoordinateReferenceSystem coordinateReferenceSystem)
			throws IOException {
		final Path prjFile = Paths.get(new StringBuffer(baseFile).append(".prj") //$NON-NLS-1$
				.toString());
		allFiles.add(prjFile);
		try(BufferedWriter out = Files.newBufferedWriter(prjFile)){
			out.write(coordinateReferenceSystem.toWKT());
		}
		
	}

	/**
	 * Creates a raster based on the list of values for band 0 
	 * @return {@link WritableRaster}
	 * @throws Exception 
	 */
	private BufferedImage createRaster() throws Exception {

		DataBuffer raster = new DataBufferFloat(this.width * this.height);
		//initialize data to no data
		for (int y = 0; y < height; y++) {
			for (int x = 0; x < width; x++) {
				raster.setElemFloat(x + y * width, NO_DATA);
			}
		}
		
		//add data points
		for (GridResultItem item : table) {
			if (item.getTileX() >= metadata.getMinXTile() && item.getTileX() <= metadata.getMaxXTile() && item.getTileY() >= metadata.getMinYTile() && item.getTileY() <= metadata.getMaxYTile()){
				int x = (int)(item.getTileX() - metadata.getMinXTile());
				int y = (int)(height - (item.getTileY() - metadata.getMinYTile() +1));
				raster.setElemFloat(x  + y * width, (float)item.getValue());
			}
		}			
		
		
		ColorSpace cs = ColorSpace.getInstance(ColorSpace.CS_GRAY);
		ColorModel cm = new ComponentColorModel(cs, false, true, Transparency.OPAQUE,DataBuffer.TYPE_FLOAT);
		SampleModel sampleModel = new ComponentSampleModel(DataBuffer.TYPE_FLOAT, width, height, 1, width, new int[] {0});
		WritableRaster wraster = Raster.createWritableRaster(sampleModel, raster, null);
		BufferedImage newImg = new BufferedImage(cm, wraster, true, null);
		return newImg;
	}

	/**
	 * 
	 * @return the built raster file
	 */
	public Path getImageFile() {
		assert this.file != null;
		return this.file;
	}
	
	/**
	 * 
	 * @return a list of all files generated by this builder
	 */
	public List<Path> getAllFiles(){
		return this.allFiles;
	}
}
