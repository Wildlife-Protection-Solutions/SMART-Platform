package org.wcs.smart.ui.internal.ca.create;


import org.eclipse.jface.fieldassist.ControlDecoration;
import org.eclipse.jface.fieldassist.FieldDecorationRegistry;
import org.eclipse.jface.wizard.WizardPage;
import org.eclipse.swt.SWT;
import org.eclipse.swt.widgets.Control;
import org.wcs.smart.ca.ConservationArea;

/**
 * Conservation area wizard page.
 * 
 * @author Emily
 *
 */
public abstract class CaWizardPage extends WizardPage{

	/**
	 * Creates a new wizard page with the given name.
	 * @param pageName
	 */
	protected CaWizardPage(String pageName) {
		super(pageName);
	}

	/**
	 * Updates the conservation area with the information
	 * specific to the given wizard page.
	 * @param ca
	 */
	abstract void updateConservationArea(ConservationArea ca);
	
	public void initControls(ConservationArea ca){}
	
	/*
	 * Creates a control decoration for a wizard page field.
	 */
	protected ControlDecoration createDecoration(Control control){
		ControlDecoration cd = new ControlDecoration(control, SWT.LEFT);
		cd.setImage(FieldDecorationRegistry.getDefault()
				.getFieldDecoration(FieldDecorationRegistry.DEC_ERROR).getImage());
		cd.setShowHover(true);
		return cd;
	}
}
