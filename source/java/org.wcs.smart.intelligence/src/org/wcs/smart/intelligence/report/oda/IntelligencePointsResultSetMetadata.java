package org.wcs.smart.intelligence.report.oda;

import org.eclipse.datatools.connectivity.oda.IResultSetMetaData;
import org.eclipse.datatools.connectivity.oda.OdaException;
import org.wcs.smart.intelligence.internal.Messages;

/**
 * SMART intelligence location points result set metadata.
 * 
 * @author elitvin
 * @since 3.0.0
 */
public class IntelligencePointsResultSetMetadata implements IResultSetMetaData {

	public static final String ID = "org.wcs.smart.intelligence.report.oda.SmartIntelligencePoints"; //$NON-NLS-1$
	
	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnCount()
	 */
	@Override
	public int getColumnCount() throws OdaException {
		return 3;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnDisplayLength(int)
	 */
	@Override
	public int getColumnDisplayLength(int arg0) throws OdaException {
		return -1;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnLabel(int)
	 */
	@Override
	public String getColumnLabel(int index) throws OdaException {
		switch(index){
		case 1: return Messages.IntelligencePointsResultSetMetadata_Column_X;
		case 2: return Messages.IntelligencePointsResultSetMetadata_Column_Y;
		case 3: return IntelligencePointsResultSet.GEOM_COLUMN_NAME;
		}
		return null;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnName(int)
	 */
	@Override
	public String getColumnName(int index) throws OdaException {
		switch(index){
		case 1: return "X"; //$NON-NLS-1$
		case 2: return "Y"; //$NON-NLS-1$
		case 3: return IntelligencePointsResultSet.GEOM_COLUMN_NAME;
		}
		return null;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnType(int)
	 */
	@Override
	public int getColumnType(int index) throws OdaException {
		switch(index){
		case 1: return java.sql.Types.DOUBLE;
		case 2: return java.sql.Types.DOUBLE;
		case 3: return java.sql.Types.JAVA_OBJECT;
		}
		return -1;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getColumnTypeName(int)
	 */
	@Override
	public String getColumnTypeName(int index) throws OdaException {
		 int nativeTypeCode = getColumnType( index );
	     return SmartIntelligenceDriver.getNativeDataTypeName( nativeTypeCode, ID );
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getPrecision(int)
	 */
	@Override
	public int getPrecision(int arg0) throws OdaException {
		return -1;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#getScale(int)
	 */
	@Override
	public int getScale(int arg0) throws OdaException {
		return -1;
	}

	/**
	 * @see org.eclipse.datatools.connectivity.oda.IResultSetMetaData#isNullable(int)
	 */
	@Override
	public int isNullable(int arg0) throws OdaException {
		return columnNullableUnknown;
	}

}
